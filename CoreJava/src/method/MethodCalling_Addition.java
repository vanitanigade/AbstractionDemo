package method;

public class MethodCalling_Addition {

	int add(int a, int b) {
		return a+b;
	}	
	public float Substraction ( float a, float b) {
			return a-b;
	}
	
	int division (int a, int b) {
		return a/b;
	}
	
	int multiply (int a, int b) {
		return a*b;
	}
	
	int mod (int a, int b) {
		return a%b;
	}
	
	
}
	


